                <p class="news_p_inf">
                    Больше двадцати лет, с 2001 года, Виктор Олегович Пелевин не появляется на публике.
                    Это затворничество породило слухи, домыслы, журналистские расследования и мистификации с раздачей
                    фейковых автографов – но все связанные с писателем-затворником новости так или иначе сводились и 
                    сводятся к тому, что людям хочется узнать о нём больше. Выражаясь по-пелевински, восполнить пустоту. 
                    Мы попросили о восполнении нейросети – и получили восемь ответов на вопрос, как выглядел автор в те 
                    годы, когда его никто не видел.
                </p>
